package Lab4;

/**
 * Vocabularies class with both prompt and answer
 */
public class Vocabulary {
    String prompt;
    String answer;
    /**
     * Create vocabulary
     *
     * @param prompt prompt vocabulary
     * @param answer answer vocabulary
     */
    public Vocabulary(String prompt, String answer) {
        this.prompt = prompt;
        this.answer = answer;
    }
}
